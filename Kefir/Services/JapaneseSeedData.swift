struct JapaneseSeedData {

    static func seedIfEmpty(context: ModelContext) {
        let desc = FetchDescriptor<Deck>()
        let count = (try? context.fetchCount(desc)) ?? 0
        if count == 0 {
            seedAll(context: context)
            EnglishSeedData.seed(context: context)
            try? context.save()
        }
    }

    static func seedAll(context: ModelContext) {
        let existingDesc = FetchDescriptor<Deck>()
        let existing = (try? context.fetch(existingDesc)) ?? []
        let existingNames = Set(existing.map { $0.name })

        let all: [(String, String, [(String, String, String, String)])] = [
            ("Хирагана", "46 базовых знаков хираганы", hiragana),
            ("Катакана", "46 базовых знаков катаканы", katakana),
            ("N5 — Числа и время", "Числа, дни, время", numbersTime),
            ("N5 — Базовые фразы", "Приветствия, вежливость", phrases),
            ("N5 — Существительные", "Базовые слова JLPT N5", nounsN5),
            ("N5 — Глаголы", "Базовые глаголы JLPT N5", verbsN5),
            ("N5 — Прилагательные", "い- и な-прилагательные", adjectivesN5),
            ("N5 — Семья и люди", "Члены семьи, профессии", family)
        ]
        for (name, desc, entries) in all {
            if existingNames.contains(name) { continue }
            createDeck(context: context, name: name, description: desc, entries: entries)
        }
    }

    private static func createDeck(
        context: ModelContext,
        name: String,
        description: String,
        entries: [(String, String, String, String)]  // kanji, kana, romaji, meaning
    ) {
        let deck = Deck(
            name: name,
            description: description,
            mode: .japanese,
            frontLanguage: "ja-JP",
            backLanguage: "ru-RU"
        )
        context.insert(deck)
        for (k, kn, r, m) in entries {
            let card = Card(deck: deck, kanji: k, kana: kn, romaji: r, meaning: m)
            deck.cards.append(card)
            context.insert(card)
        }
    }

